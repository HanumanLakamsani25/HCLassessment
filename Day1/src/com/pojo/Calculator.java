public class Calculator {
	int num1;
	int num2;


	public int Addition() {
		return num1+num2;
	}
	public int Subtract() {
		return num1-num2;
	}
	public int Multiply() {
		return num1*num2;
	}
	public int Divide() {
		return num1/num2;
	}
	public int Remainder() {
		return num1%num2;
	}
}
